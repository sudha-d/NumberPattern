package com.company;

public class SquareStarPattern {
    public static void main(String[] args) {
        int j;
        int i;
        for (j = 1; j <= 5; j++) {
            for (i = 1; i <= 5; i++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}


